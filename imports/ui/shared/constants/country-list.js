export function getFlagName(code) {
  const country = COUNTRY_LIST.find((x) => x.value === code)
  return country?.label
}

export function getCode(name) {
  const country = COUNTRY_LIST_RAW.find((x) => x.label === name)
  return country?.value
}

export function getCountryByGUID(country) {
  const countryCode = COUNTRY_LIST_RAW_GUID.find(
    (x) => x.label === country,
  )?.value

  return countryCode ? countryCode : ''
}

export function getFlag(code) {
  return FLAG_LIST[code]
}

export function getName(code) {
  const country = COUNTRY_LIST_RAW.find(
    (x) => x.value === code,
  )
  return country?.label
}

export function getCountryFields(code, city) {
  const countryFields = {}

  countryFields.countryName = getName(code)
  countryFields.countryFlag = getFlag(code)
  countryFields.countryFlagName = getFlagName(code)
  if (city) {
    countryFields.cityCountry = `${city}, ${getName(code)}`
    countryFields.countryCity = `${getName(code)}, ${city}`
  }
  return countryFields
}
